package com.Group6.checkup.TableClassPackage;

public class OnlineHelp {

    private int onlineHelpID;
    private String messageTitle;
    private String message;
    private String sentDateTime;
    private int patientID;
    private int doctorID;
    private int onlineHelpReplyID;

    public int getOnlineHelpID() {
        return onlineHelpID;
    }

    public void setOnlineHelpID(int onlineHelpID) {
        this.onlineHelpID = onlineHelpID;
    }

    public String getMessageTitle() {
        return messageTitle;
    }

    public void setMessageTitle(String messageTitle) {
        this.messageTitle = messageTitle;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getSentDateTime() {
        return sentDateTime;
    }

    public void setSentDateTime(String sentDateTime) {
        this.sentDateTime = sentDateTime;
    }

    public int getPatientID() {
        return patientID;
    }

    public void setPatientID(int patientID) {
        this.patientID = patientID;
    }

    public int getDoctorID() {
        return doctorID;
    }

    public void setDoctorID(int doctorID) {
        this.doctorID = doctorID;
    }

    public int getOnlineHelpReplyID() {
        return onlineHelpReplyID;
    }

    public void setOnlineHelpReplyID(int onlineHelpReplyID) {
        this.onlineHelpReplyID = onlineHelpReplyID;
    }
}
